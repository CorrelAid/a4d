test_that("parse_date_string works as expected", {
    expect_equal(parse_dates("2020-01-01"), as.Date("2020-01-01"))
    expect_equal(parse_dates("May-20"), as.Date("2020-05-01"))
    expect_equal(parse_dates("May-99"), as.Date("1999-05-01"))
    expect_equal(parse_dates("Jan-2021"), as.Date("2021-01-01"))
    expect_equal(parse_dates("June2020"), as.Date("2020-06-01"))
    expect_equal(parse_dates("June-2020"), as.Date("2020-06-01"))
    expect_equal(parse_dates("08/05/2020"), lubridate::ymd("2020-05-08"))
    expect_equal(parse_dates("6/10/17"), lubridate::ymd("2017-10-06"))
    expect_equal(parse_dates("2020-05-08"), lubridate::ymd("2020-05-08"))
})
